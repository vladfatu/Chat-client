    private void adaugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adaugaActionPerformed
        boolean error;
        error=false;
        for (int i=0;i<n;i++)
            if (prieten_nou.getText().equals(((JLabel)model.getElementAt(i)).getText()))
            {
                error=true;
            }
        if (error) JOptionPane.showMessageDialog(Meniu.m,"Aceasta porecla exista deja in lista","Eroare",JOptionPane.ERROR_MESSAGE);
        else
        {
            try {
                stream = new DataOutputStream(s.getOutputStream());
            } catch (IOException ex) {
                System.out.println("Streamul nu poate fi creat");
            }
            try {
                stream.writeUTF("/cauta "+prieten_nou.getText());
            } catch (IOException ex) {
                System.out.println("textul nu poate fi scris");
                return;
            }
        }
        prieten_nou.setText("");
    }//GEN-LAST:event_adaugaActionPerformed

