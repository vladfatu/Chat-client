class LabelListCellRenderer extends DefaultListCellRenderer {

    public LabelListCellRenderer(){
        super();
    }

    @Override
    public Component getListCellRendererComponent(JList list, Object value,
            int index, boolean isSelected, boolean hasFocus) {
        JLabel j=new JLabel();
        if(JLabel.class.isInstance(value))
        {
            j= (JLabel)value;
            if (isSelected) {j.setBackground(Color.BLACK);j.setForeground(Color.blue);}
            else {j.setBackground(Color.white);j.setForeground(Color.black);}
            return j;
        }
        return super.getListCellRendererComponent(list, value,
                index, isSelected, hasFocus);
    }

}

